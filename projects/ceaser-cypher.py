alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z', 'a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']

def ceaser(startText, shift, direction):
    endText = ""
    if direction == "decrypt":
        shift*=-1
    
    for i in startText:
        if i in alphabet:
            position = alphabet.index(i)
            newPosition = position + shift
            endText += alphabet[newPosition]
        else:
            endText += i
    
    print("Here is the "+direction+"ed message: "+endText)

text = input("Enter the word you want to cipher: ")
text=text.lower()
directionED = input("Enter encrypt or decrypt: ")
shiftAmount = int(input("Enter the shift amount: "))
shiftAmount = shiftAmount%26

ceaser(startText=text, shift=shiftAmount, direction=directionED)